public class Exercitiul4 {
    public static void main(String[] args) {
        int n = 354;
        int i = 1;
        int sum = 0;
        System.out.println("Afiseaza suma totala:");
        while (i <= n) {
            sum = sum + i;
            i++;
        }
        System.out.println(sum);
    }
}
